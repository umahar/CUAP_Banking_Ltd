class CheckInput:
    def __init__(self) -> None:
        pass

    @staticmethod
    def is_valid_email(email):
        return True

    @staticmethod
    def is_valid_password(password):
        return False

    @staticmethod
    def is_valid_phone_no(phone_no):
        return False

    @staticmethod
    def is_valid_name(name):
        return False

    @staticmethod
    def is_valid_gender(gender):
        return False

    @staticmethod
    def is_valid_deposit(deposit):
        return False

    @staticmethod
    def is_valid_acc_type(acc_type):
        return False

    @staticmethod
    def is_valid_dob(dob):
        return False
